            <h2>Camille Pizarro</h2>
            <img src="imgs/artistas/camillePizarro/camillePizarro.png" alt="">
            

            <h6>Camille Pissarro realizó una amplia actividad plástica que abarcaba las más diversas técnicas, desde el óleo y la acuarela hasta la litografía y el aguafuerte. Su obra conforma uno de los más brillantes conjuntos pictóricos en el ámbito del paisaje impresionista, tanto rural como urbano.

                Antes de establecerse en Francia, pasó un tiempo en Venezuela con el pintor danés Melbye, dedicándose plenamente a la pintura. Ya en París (1855), recibió los consejos de Camille Corot y trabó amistad con Claude Monet, Paul Cézanne, Armand Guillaumin y, años más tarde, con Paul Gauguin. Su estilo en esta época era bastante tradicional. En el transcurso de una estancia en Londres (1870-1871) conoció la obra de Joseph Turner, y a su regreso a Francia participó en el movimiento impresionista. Su entusiasmo lo llevó a ser uno de los principales impulsores de la exposición celebrada en el estudio del fotógrafo Nadar en 1874, a raíz de la cual la nueva tendencia pictórica recibiría la denominación por la cual se la conocería.


            </h6>

