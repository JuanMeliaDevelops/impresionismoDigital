        <h3>Que es el Impresionismo?</h3>

        <h6>La pintura impresionista nace a partir de la segunda mitad del siglo XIX y quiere, a grandes rasgos, plasmar
            la luz y el instante, sin importar demasiado la identidad de aquello que la proyectaba. Las cosas no se
            definen, sino que se pinta la impresión visual de estas cosas, y eso implica que las partes inconexas dan
            lugar a un todo unitario (algo que años después demostraría científica y psicológicamente la Gestalt).</h6>

        <img src="imgs/queEs.jpg" alt="">

        <h6 class="queEsInfo"> Este movimiento se caracteriza por el uso de colores puros sin mezclar (todo color es
            relativo a los colores que le rodean), el hecho de no ocultar la pincelada, y por supuesto darle
            protagonismo ante todo a la luz y el color. De esta manera las formas se diluyen imprecisas dependiendo de
            la luz a la que están sometidas, y una misma forma cambia dependiendo de la luz arrojada sobre ellas, dando
            lugar a una pintura totalmente distinta.

            Por ello, y a partir de los paisajistas de la escuela de Barbizon, los impresionistas se centraron en la
            pintura al aire libre, buscando plasmar el cambio de la luminosidad, el instante.
        </h6>


